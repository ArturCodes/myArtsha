function show() {
    document.getElementById('navbar-links').style.width = "240px"
    document.getElementById('opacity-bg').style.display = "block"
    document.getElementById('mobile-menu-close').style.display = "block"
}

function hide() {
    document.getElementById('navbar-links').style.width = "0px"
    document.getElementById('opacity-bg').style.display = "none"
    document.getElementById('mobile-menu-close').style.display = "none"
}
